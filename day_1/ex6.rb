# String interpolation MUST be done with double quotes

# Number of types of people in the world
types_of_people = 10

# First line of joke
x = "There are #{types_of_people} types of people."

# Declare variable for binary string
binary = "binary"

# Declare variable for don't string
do_not = "don't"

# Joke punchline
y = "Those who know #{binary} and those who #{do_not}."

# Output joke to terminal
puts x
puts y

# Repeat joke
puts "I said: #{x}."
puts "I also said: '#{y}'."

# Evaluate joke and store as string
hilarious = false
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# Output joke evaluation to terminal
puts joke_evaluation

# Declare string variables to represent sides of a string
w = "This is the left side of..."
e = "a string with a right side."

# Concatenate strings and output to terminal
puts w + e
